/*
 * DnCNN int8 kernel for ET-SoC1 - the `dncnn` leaderboard model (config-driven).
 * TFMA = the int8 tensor fused-multiply-add engine op that runs the hidden convolutions.
 *
 *   conv_first+quantize (FP32) -> 18 hidden convs (int8 TFMA) -> dequantize+conv_final (FP32) + residual
 *
 * The network shape (layer count, per-layer in/out channels, layer kind) is a compile-time
 * descriptor (the NET[] table) and the compute primitives are reusable functions parameterized by
 * dims and SCP (the 48-line L1 scratchpad) line offsets, so the same source can build other
 * channel counts. Shipped config: DNCNN_REAL=1, CH=64, HIDDEN=18, IMG=64,
 * ACTIVE_HARTS=8 - the real deepinv dncnn_sigma2_gray model.
 *
 * Techniques used (int8 TFMA, output-channel tiling, adaptive tap-fold, word-copy activation pack,
 * batched cache evict, cacheline seam fix, multi-hart row banding, FP32 mixed-precision boundaries
 * with folded bias): docs/OPTIMIZATIONS.md.
 *
 * Board memory map (REAL): input @0x2000, output @0x10000, weight blob @0x14000; scales from the
 * generated dncnn_int8_scales_real.h. Self-attesting summary @0x1000 for the CI dump gate.
 */

